namespace Mathlib
{
    static public class MathEngine
    {
        public static int Addition(int a, int b)
        {
            return a + b;
        }

        public static int Subtration(int a, int b)
        {
            return a - b;
        }

       public static int Multiplication(int a, int b)
        {
            return a * b;
        }
    }
}
